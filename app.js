function moveProductGroup(encodedGroup, index) {
  const group = decodeURIComponent(encodedGroup);
  const oldTarget = splitGroupKey(group);
  const nextCategory = normalizeCategoryName(document.getElementById('moveCat_' + index)?.value || '');
  const nextSub = normalizeSubName(nextCategory, document.getElementById('moveSub_' + index)?.value || '');

  if (!nextCategory || !nextSub) return alert('Kategori dan sub tujuan wajib diisi.');
  if (oldTarget.category === nextCategory && oldTarget.sub === nextSub) {
    return alert('Tujuan masih sama dengan grup sekarang.');
  }
  if (!confirm(`Pindahkan semua barang dari "${oldTarget.category} / ${oldTarget.sub}" ke "${nextCategory} / ${nextSub}"?`)) return;

  const products = getProducts();
  let moved = 0;
  products.forEach(product => {
    if (!productMatchesGroup(product, group)) return;
    product.category = nextCategory;
    product.sub = nextSub;
    product.brand = nextSub;
    moved += 1;
  });

  if (!moved) return alert('Tidak ada barang yang cocok untuk dipindahkan.');

  setProducts(products);
  syncCategoriesWithProducts();
  renderCategoryAdmin();
  refreshCategoryOptions();
  renderAdmin();
  renderProducts();
  renderStockReport();
  alert(`${moved} barang dipindahkan ke ${nextCategory} / ${nextSub}.`);
}

