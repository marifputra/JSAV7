async function checkout() {
  const activeMember = getMember();
  if (!activeMember) return alert('Silakan login member dulu.');
  const profile = saveCheckoutProfile();
  if (!profile.name) return alert('Nama pembeli wajib diisi sebelum checkout.');
  if (!profile.phone) return alert('Nomor telepon pembeli wajib diisi sebelum checkout.');
  if (profile.phone.length < 8) return alert('Nomor telepon terlalu pendek.');
  const member = {
    id: profile.phone,
    username: profile.phone,
    name: profile.name,
    phone: profile.phone,
    level: activeMember.level || 'MEMBER 3',
    accountUsername: activeMember.username || activeMember.id || '',
    accountName: activeMember.name || '',
    joinedAt: new Date().toLocaleString('id-ID')
  };
  if (!cart.length) return alert('Keranjang kosong.');

  if (SERVER_MODE) {
    try {
      const res = await fetch(API_BASE + '/api/checkout', {
        method: 'POST',
        headers: apiHeaders({ 'Content-Type': 'application/json' }),
        body: JSON.stringify({ member, cart })
      });
      const result = await res.json();
      if (!res.ok || !result.ok) return alert(result.error || 'Checkout gagal.');

      syncingState = true;
      applyServerState(result.state);
      syncingState = false;

      makeReceiptPdf(result.order);
      alert('Checkout berhasil. Stok dikurangi di server lokal.');
      rememberBuyer(member);
      clearCart();
      await loadStateFromServer();
      renderProducts();
      renderStats();
      renderStockReport();
      updateStockBadge();
      return;
    } catch (err) {
      syncingState = false;
      return alert('Checkout gagal terhubung ke server lokal: ' + err.message);
    }
  }

  const products = getProducts();

  for (const item of cart) {
    const p = products.find(x => String(x.id) === String(item.id));
    const saleMode = item.saleType === 'besar' ? 'big' : 'small';
    const qtySmall = saleMode === 'big' ? n(item.displayQty) * n(p?.contentPerBig || 1) : n(item.displayQty);
    if (!p) return alert('Produk ' + item.name + ' sudah dihapus admin.');
    if (qtySmall <= 0) return alert('Jumlah ' + p.name + ' tidak valid.');
    if (qtySmall > p.stockSmall) return alert('Stok ' + p.name + ' tidak cukup. Tersedia ' + formatStock(p));
  }

  const items = cart.map(item => {
    const p = products.find(x => String(x.id) === String(item.id));
    const saleMode = item.saleType === 'besar' ? 'big' : 'small';
    const displayQty = n(item.displayQty);
    const qtySmall = saleMode === 'big' ? displayQty * n(p.contentPerBig || 1) : displayQty;
    const displayUnit = saleMode === 'big' ? p.bigUnit : p.smallUnit;
    const displayPrice = getMemberPrice(p, saleMode);
    const subtotal = displayPrice * displayQty;
    const modal = p.cost * qtySmall;

    p.stockSmall = +(n(p.stockSmall) - n(qtySmall)).toFixed(2);

    return {
      id: p.id,
      name: p.name,
      saleType: item.saleType,
      displayQty,
      displayUnit,
      displayPrice,
      qtySmall,
      smallUnit: p.smallUnit,
      priceSmall: p.priceSmall,
      costSmall: p.cost,
      subtotal,
      modal,
      profit: subtotal - modal
    };
  });

  setProducts(products);

  const total = items.reduce((s, i) => s + i.subtotal, 0);
  const modal = items.reduce((s, i) => s + i.modal, 0);
  const profit = total - modal;

  const orders = getOrders();
  const order = {
    id: Date.now(),
    date: new Date().toLocaleString('id-ID'),
    memberName: member.name,
    memberPhone: member.phone,
    memberLevel: member.level,
    items,
    total,
    modal,
    profit,
    points: Math.floor(total / 100000)
  };
  orders.unshift(order);
  setOrders(orders);
  rememberBuyer(member);

  makeReceiptPdf(order);

  alert('Checkout berhasil. Stok otomatis berkurang dan struk PDF dibuat.');
  clearCart();
  renderProducts();
}

