function renderAdmin() {
  const list = document.getElementById('adminList');
  if (!list) return;

  const q = (document.getElementById('adminSearch')?.value || '').toLowerCase().trim();
  const products = getProducts();

  let filtered = products.filter(p => {
    const text = `${p.name} ${p.brand || ''} ${p.category} ${p.sub} ${p.id}`.toLowerCase();
    return !q || q.split(/\s+/).every(w => text.includes(w));
  });

  const totalPages = Math.max(1, Math.ceil(filtered.length / adminPerPage));
  if (adminPage > totalPages) adminPage = totalPages;

  const start = (adminPage - 1) * adminPerPage;
  const pageItems = filtered.slice(start, start + adminPerPage);

  const info = document.getElementById('adminPageInfo');
  if (info) info.textContent = `Halaman ${adminPage} / ${totalPages} - ${filtered.length} barang`;

  const grouped = {};
  pageItems.forEach(p => {
    const key = `${p.category} / ${p.sub}`;
    if(!grouped[key]) grouped[key] = [];
    grouped[key].push(p);
  });

  const categoryOptions = getCategories()
    .map(c => `<option value="${escapeHtml(c.name)}"></option>`)
    .join('');

  list.innerHTML = `
    <datalist id="categoryMoveList">${categoryOptions}</datalist>
    ${Object.entries(grouped).map(([group, items], groupIndex) => {
    const isGroupOpen = !!openProductGroups[group];
    return `
      <div class="admin-cat compact-cat group-card">
        <button class="group-head" onclick="toggleProductGroup('${encodeURIComponent(group)}')">
          <span>
            <b>${escapeHtml(group)}</b>
            <small>${items.length} barang di halaman ini</small>
          </span>
          <span class="group-arrow ${isGroupOpen ? 'is-open' : ''}" aria-hidden="true"></span>
        </button>
        <div class="compact-list ${isGroupOpen ? '' : 'hidden'}">
          ${groupOrganizerHtml(group, groupIndex)}
          ${items.map(p => productAccordion(p)).join('')}
        </div>
      </div>
    `;
  }).join('') || '<p class="muted">Produk tidak ditemukan.</p>'}
  `;

  renderStats();
}

