function splitGroupKey(group = '') {
  const parts = String(group || '').split(' / ');
  const category = normalizeCategoryName(parts.shift() || '');
  return {
    category,
    sub: normalizeSubName(category, parts.join(' / ') || '')
  };
}

function productMatchesGroup(product, group) {
  const target = splitGroupKey(group);
  return normalizeCategoryName(product.category) === target.category
    && normalizeSubName(product.category, product.sub) === target.sub;
}

