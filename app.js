function formatStock(product) {
  const big = Math.floor(n(product.stockSmall) / n(product.contentPerBig || 1));
  const small = +(n(product.stockSmall) % n(product.contentPerBig || 1)).toFixed(2);
  const bigUnit = escapeHtml(product.bigUnit);
  const smallUnit = escapeHtml(product.smallUnit);

  if (big > 0 && small > 0) return `${big} ${bigUnit} ${small} ${smallUnit}`;
  if (big > 0) return `${big} ${bigUnit}`;
  return `${small} ${smallUnit}`;
}

function calcStockSmall(stockBig, stockSmall, contentPerBig) {
  return +(n(stockBig) * n(contentPerBig || 1) + n(stockSmall)).toFixed(2);
}

function splitStock(product) {
  const content = n(product.contentPerBig || 1) || 1;
  return {
    big: Math.floor(n(product.stockSmall) / content),
    small: +(n(product.stockSmall) % content).toFixed(2)
  };
}

