function renderGroupedStock(rows, key){
  const groups = [...new Set(rows.map(r => r[key] || 'Lainnya'))].sort();
  if(!groups.length) return '<p class="muted">Data stok tidak ditemukan.</p>';

  return groups.map(group => {
    const items = rows.filter(r => (r[key] || 'Lainnya') === group);
    const warning = items.filter(r => r.status === 'MENIPIS').length;
    const habis = items.filter(r => r.status === 'HABIS').length;

    return `
      <div class="stock-group">
        <button class="sub-head" onclick="toggleStockGroup('${escapeHtml(group).replaceAll("'","&#039;")}')">
          <span>${escapeHtml(group)}</span>
          <b>${items.length} barang - ${warning} menipis - ${habis} habis</b>
        </button>
        <div class="sub-content hidden" data-stock-group="${escapeHtml(group)}">
          ${renderStockTable(items, group)}
        </div>
      </div>
    `;
  }).join('');
}

