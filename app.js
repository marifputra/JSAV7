async function renderProductsFromServer() {
  const grid = document.getElementById('productGrid');
  if (!grid) return false;

  const meta = await loadPublicCatalogMeta();
  if (!meta) return false;

  const q = (document.getElementById('search')?.value || '').toLowerCase().trim();
  const cats = meta.productCategories?.length ? meta.productCategories : ['Semua', ...(meta.categories || []).map(c => c.name)];
  document.getElementById('filters').innerHTML = cats.map(c =>
    `<button class="chip ${active === c ? 'active' : ''}" onclick="setActiveCategory('${encodeURIComponent(c)}')">${escapeHtml(c)}</button>`
  ).join('');

  const brandBox = document.getElementById('brandFilters');
  if (brandBox) {
    const brands = meta.brands?.length ? meta.brands : ['Semua'];
    brandBox.innerHTML = (active === 'Rokok' || q)
      ? brands.map(b => `<button class="chip ${activeMerek === b ? 'active' : ''}" onclick="setActiveMerek('${encodeURIComponent(b)}')">${escapeHtml(b)}</button>`).join('')
      : '';
  }

  const seq = ++productFetchSeq;
  grid.innerHTML = '<p class="muted">Memuat produk...</p>';

  const params = new URLSearchParams({
    page: String(productPage),
    limit: String(productPerPage)
  });
  if (q) params.set('q', q);
  if (active && active !== 'Semua') params.set('category', active);
  if (activeMerek && activeMerek !== 'Semua') params.set('brand', activeMerek);

  try {
    const res = await fetch(API_BASE + '/api/products?' + params.toString(), { cache: 'no-store' });
    if (!res.ok) throw new Error('Produk tidak tersedia');
    const data = await res.json();
    if (seq !== productFetchSeq) return true;

    const products = (data.products || []).map(normalizeProduct);
    localStorage.setItem('jsa_products', JSON.stringify(products));

    const totalPages = Math.max(1, Math.ceil(n(data.total) / productPerPage));
    if (productPage > totalPages) {
      productPage = totalPages;
      return renderProductsFromServer();
    }

    grid.innerHTML = products.map(productCardHtml).join('') || '<p>Produk tidak ditemukan. Coba kata pencarian lain.</p>';

    const pager = document.getElementById('productPager');
    if (pager) {
      pager.innerHTML = n(data.total) > productPerPage ? `
        <button class="btn secondary" onclick="setProductPage(productPage - 1)" ${productPage <= 1 ? 'disabled' : ''}>Sebelumnya</button>
        <span class="hint">Halaman ${productPage} / ${totalPages} - ${n(data.total)} barang</span>
        <button class="btn secondary" onclick="setProductPage(productPage + 1)" ${productPage >= totalPages ? 'disabled' : ''}>Berikutnya</button>
      ` : '';
    }
    return true;
  } catch (err) {
    console.warn('Gagal memuat produk server, memakai data browser:', err);
    return false;
  }
}

